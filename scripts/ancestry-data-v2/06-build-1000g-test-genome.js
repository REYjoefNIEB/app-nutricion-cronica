#!/usr/bin/env node
/**
 * Builds a synthetic test genome for a specific 1000G individual (HG00096, GBR)
 * by querying Ensembl REST API for genotypes at all 141 CLG AIM positions.
 *
 * Output: scripts/test-data/adn-C-HG00096-GBR.txt (23andMe-like TSV format)
 * Run: node scripts/ancestry-data-v2/06-build-1000g-test-genome.js
 */
'use strict';

const https = require('https');
const fs    = require('fs');
const path  = require('path');

const { AIMS } = require(path.join(__dirname, '..', '..', 'functions', 'ancestry', 'referenceData_v2'));
const OUTPUT    = path.join(__dirname, '..', 'test-data', 'adn-C-HG00096-GBR.txt');

const TARGET_SAMPLE = '1000GENOMES:phase_3:HG00096';  // British (GBR) male

function fetchGenotypes(rsid) {
    return new Promise((resolve) => {
        const options = {
            hostname: 'rest.ensembl.org',
            path:     `/variation/human/${rsid}?genotypes=1;content-type=application/json`,
            method:   'GET',
            headers:  { 'User-Agent': 'NURA-research/2.0' }
        };

        const req = https.request(options, (res) => {
            let data = '';
            res.on('data', chunk => { data += chunk; });
            res.on('end', () => {
                if (res.statusCode === 429) { resolve({ _rateLimited: true }); return; }
                try { resolve(JSON.parse(data)); }
                catch (e) { resolve({ _error: e.message }); }
            });
        });
        req.on('error', e => resolve({ _error: e.message }));
        req.setTimeout(20000, () => { req.destroy(); resolve({ _timeout: true }); });
        req.end();
    });
}

function extractGenotype(data, targetSample) {
    if (!data.genotypes) return null;
    const entry = data.genotypes.find(g => g.sample === targetSample);
    if (!entry?.genotype) return null;
    // Genotype is phased (e.g., "A|G" or "T|T") — strip phase separator
    const alleles = entry.genotype.replace('|', '').replace('/', '');
    if (alleles.length !== 2) return null;
    return alleles;
}

async function main() {
    console.log(`Building test genome for ${TARGET_SAMPLE}`);
    console.log(`Querying ${AIMS.length} AIMs from Ensembl...\n`);

    const rows = [];
    let found = 0, missing = 0;

    for (let i = 0; i < AIMS.length; i++) {
        const aim = AIMS[i];

        let data, attempts = 0;
        while (attempts < 3) {
            data = await fetchGenotypes(aim.rsid);
            if (data._rateLimited) {
                console.log(`  ⏳ Rate limit — waiting 60s...`);
                await new Promise(r => setTimeout(r, 60000));
                continue;
            }
            if (data._timeout || data._error) {
                await new Promise(r => setTimeout(r, 3000 * (attempts + 1)));
                attempts++;
                continue;
            }
            break;
        }

        const gt = extractGenotype(data, TARGET_SAMPLE);

        if (gt) {
            rows.push(`${aim.rsid}\t${aim.chromosome}\t${aim.position}\t${gt}`);
            found++;
            process.stdout.write(`[${i+1}/${AIMS.length}] ${aim.rsid}: ${gt}\n`);
        } else {
            process.stdout.write(`[${i+1}/${AIMS.length}] ${aim.rsid}: NOT FOUND\n`);
            missing++;
        }

        // Rate limit: ~3 req/s
        await new Promise(r => setTimeout(r, 350));
    }

    // Write output in 23andMe-like format
    const header = `# This data file generated by NURA research pipeline
# Source: 1000 Genomes Phase 3, Ensembl REST API
# Individual: ${TARGET_SAMPLE} (British, GBR population)
# Chip: Whole genome sequence (WGS, phased)
# Reference: GRCh38 (same as Ensembl frequencies used in CLG panel)
# Generated: ${new Date().toISOString()}
#
# rsid\tchromosome\tposition\tgenotype
`;

    fs.writeFileSync(OUTPUT, header + rows.join('\n') + '\n');

    console.log(`\n${'═'.repeat(50)}`);
    console.log(`✅ Done: ${found} AIMs found, ${missing} missing`);
    console.log(`Output: ${OUTPUT}`);
    console.log(`${'═'.repeat(50)}`);
}

main().catch(e => { console.error('Fatal:', e.message); process.exit(1); });
